package day03_helloworld_variables;

public class VariableDeclarations {
	public static void main(String[] args) {
		//Declare single variable
		int apples;
		
		
		//Declare multiple variables on single statement
		int bananas, oranges, mango;
		
		
		
		//Declare variable and assing value in single statement
		int kiwi = 14;
		
		System.out.println(kiwi);
		
		//Declare and asign value to multiple variables
		// in single statement
		
		int watermelon = 11, grapes = 100, cherries = 12, apricots;
		
		
		
	} 

}
